#import "SLManagerTool.h"

#import "SLHttpTool.h"
#import "MJExtension.h"

@implementation SLManagerTool

+ (void)managerCustomerListForMaterialWithParameters:(SLManageParameters *)parameters success:(void (^)(SLResult *result))success failure:(void (^)(NSError *error))failure
{
    NSString *url = [SLHttpUrl stringByAppendingString:@"/user/listCustomerVIPWithOperateStatusByMaterialId"];
    
    [SLHttpTool postWithUrlstr:url parameters:parameters.keyValues success:^(id responseObject) {
        
        SLLog(@"%@", responseObject);
        
        SLResult *result = [SLResult objectWithKeyValues:responseObject];
        
        if (success) {
            success(result);
        }
    } failure:^(NSError *error) {
        if (failure) {
            failure(error);
        }
    }];
}

@end
